public class LeetCode {
    String question;
    String answer;
    String setup;

    public LeetCode(String question, String answer, String setup) {
        this.question = question;
        this.answer = answer;
        this.setup = setup;
    }

    public String getQuestion() {
        return question;
    }

    public String getAnswer() {
        return answer;
    }

    public String getSetup() {
        return setup;
    }

}
